package codegen

import (
	"bytes"
	"fmt"
	"go/format"
	"go/token"
	"path/filepath"
	"sort"
	"strings"

	"github.com/wdsgyj/libbinder-go/internal/aidl/gomodel"
)

const defaultRuntimeImport = "github.com/wdsgyj/libbinder-go/binder"

type GoOptions struct {
	RuntimeImportPath   string
	TypeMappingsPath    string
	CustomParcelables   map[string]gomodel.CustomParcelableConfig
	GeneratedImportRoot string
}

type OutputFile struct {
	Path    string
	Content []byte
}

func RenderGo(model *gomodel.File, opts GoOptions) ([]OutputFile, error) {
	if model == nil {
		return nil, fmt.Errorf("nil model")
	}
	if len(model.ExternalRefs) != 0 {
		return nil, fmt.Errorf("unresolved external references: %s", strings.Join(model.ExternalRefs, ", "))
	}

	renderer := &goRenderer{
		model:               model,
		runtimeImportPath:   opts.RuntimeImportPath,
		typeMappingsPath:    opts.TypeMappingsPath,
		customParcelables:   opts.CustomParcelables,
		generatedImportRoot: opts.GeneratedImportRoot,
	}
	if renderer.runtimeImportPath == "" {
		renderer.runtimeImportPath = defaultRuntimeImport
	}
	renderer.indexModel()
	if err := renderer.validate(); err != nil {
		return nil, err
	}

	content, err := renderer.render()
	if err != nil {
		return nil, err
	}

	return []OutputFile{{
		Path:    outputPath(model),
		Content: content,
	}}, nil
}

type goRenderer struct {
	model               *gomodel.File
	runtimeImportPath   string
	typeMappingsPath    string
	customParcelables   map[string]gomodel.CustomParcelableConfig
	generatedImportRoot string
	buf                 bytes.Buffer
	parcelablesByName   map[string]*gomodel.Parcelable
	unionsByName        map[string]*gomodel.Union
}

type importSpec struct {
	Path  string
	Alias string
}

func (r *goRenderer) indexModel() {
	r.parcelablesByName = make(map[string]*gomodel.Parcelable, len(r.model.Parcelables))
	for _, p := range r.model.Parcelables {
		r.parcelablesByName[p.GoName] = p
	}
	r.unionsByName = make(map[string]*gomodel.Union, len(r.model.Unions))
	for _, u := range r.model.Unions {
		r.unionsByName[u.GoName] = u
	}
}

func (r *goRenderer) render() ([]byte, error) {
	r.linef("// Code generated by aidlgen. DO NOT EDIT.")
	r.linef("")
	r.linef("package %s", r.model.GoPackage)
	r.linef("")

	imports := r.imports()
	if len(imports) != 0 {
		r.linef("import (")
		for _, imp := range imports {
			if imp.Alias != "" {
				r.linef("\t%s %q", imp.Alias, imp.Path)
				continue
			}
			r.linef("\t%q", imp.Path)
		}
		r.linef(")")
		r.linef("")
	}

	r.renderParcelFileDescriptorHelpers()
	for _, enum := range r.model.Enums {
		r.renderEnum(enum)
	}
	for _, parcelable := range r.model.Parcelables {
		r.renderParcelable(parcelable)
	}
	for _, union := range r.model.Unions {
		r.renderUnion(union)
	}
	for _, iface := range r.model.Interfaces {
		r.renderInterface(iface)
	}

	out, err := format.Source(r.buf.Bytes())
	if err != nil {
		return nil, fmt.Errorf("format generated code: %w\n%s", err, r.buf.String())
	}
	return out, nil
}

func (r *goRenderer) validate() error {
	for _, p := range r.model.Parcelables {
		if !p.Structured {
			continue
		}
		for _, c := range p.Consts {
			if err := r.validateConstType(c.Type); err != nil {
				return fmt.Errorf("parcelable %s const %s: %w", p.GoName, c.GoName, err)
			}
		}
		for _, field := range p.Fields {
			if err := r.validateTypeUsage(field.Type, false); err != nil {
				return fmt.Errorf("parcelable %s field %s: %w", p.GoName, field.GoName, err)
			}
		}
	}
	for _, u := range r.model.Unions {
		for _, field := range u.Fields {
			if err := r.validateTypeUsage(field.Type, false); err != nil {
				return fmt.Errorf("union %s field %s: %w", u.GoName, field.GoName, err)
			}
		}
	}
	for _, iface := range r.model.Interfaces {
		if iface.Stable && (iface.Version <= 0 || iface.Hash == "") {
			if r.typeMappingsPath != "" {
				return fmt.Errorf("stable interface %s missing version/hash mapping in %s", iface.AIDLName, r.typeMappingsPath)
			}
			return fmt.Errorf("stable interface %s missing version/hash mapping", iface.AIDLName)
		}
		for _, c := range iface.Consts {
			if err := r.validateConstType(c.Type); err != nil {
				return fmt.Errorf("interface %s const %s: %w", iface.GoName, c.GoName, err)
			}
		}
		for _, method := range iface.Methods {
			if method.Return != nil {
				if err := r.validateTypeUsage(method.Return.Type, true); err != nil {
					return fmt.Errorf("method %s.%s return: %w", iface.GoName, method.GoName, err)
				}
			}
			for _, arg := range method.Inputs {
				if err := r.validateTypeUsage(arg.Type, true); err != nil {
					return fmt.Errorf("method %s.%s input %s: %w", iface.GoName, method.GoName, arg.GoName, err)
				}
			}
			for _, arg := range method.Outputs {
				if err := r.validateTypeUsage(arg.Type, true); err != nil {
					return fmt.Errorf("method %s.%s output %s: %w", iface.GoName, method.GoName, arg.GoName, err)
				}
			}
		}
	}
	if r.generatedImportRoot == "" && r.requiresGeneratedImports() {
		return fmt.Errorf("cross-package AIDL references require generated import root")
	}
	return nil
}

func (r *goRenderer) validateConstType(typ *gomodel.Type) error {
	if typ == nil {
		return fmt.Errorf("nil type")
	}
	switch typ.Kind {
	case gomodel.TypeBool, gomodel.TypeByte, gomodel.TypeChar, gomodel.TypeInt32, gomodel.TypeInt64, gomodel.TypeFloat32, gomodel.TypeFloat64, gomodel.TypeString, gomodel.TypeEnum:
		return nil
	default:
		return fmt.Errorf("unsupported const type %s", typ.GoExpr)
	}
}

func (r *goRenderer) validateTypeUsage(typ *gomodel.Type, allowBinderObjects bool) error {
	if typ == nil {
		return fmt.Errorf("nil type")
	}
	switch typ.Kind {
	case gomodel.TypeVoid, gomodel.TypeBool, gomodel.TypeByte, gomodel.TypeChar, gomodel.TypeInt32, gomodel.TypeInt64, gomodel.TypeFloat32, gomodel.TypeFloat64, gomodel.TypeString, gomodel.TypeParcelable, gomodel.TypeEnum, gomodel.TypeUnion:
		return nil
	case gomodel.TypeFileDescriptor, gomodel.TypeParcelFileDescriptor:
		return nil
	case gomodel.TypeSlice, gomodel.TypeArray:
		return r.validateTypeUsage(typ.Elem, allowBinderObjects)
	case gomodel.TypeMap:
		if typ.Key == nil || typ.Value == nil {
			return fmt.Errorf("map type %q is missing key/value type", typ.GoExpr)
		}
		if typ.GoExpr == "map[any]any" {
			return nil
		}
		if !r.isValidMapKeyType(typ.Key) {
			return fmt.Errorf("unsupported map key type %q", r.typeExpr(typ.Key))
		}
		if err := r.validateTypeUsage(typ.Key, allowBinderObjects); err != nil {
			return err
		}
		return r.validateTypeUsage(typ.Value, allowBinderObjects)
	case gomodel.TypeInterface, gomodel.TypeBinder:
		return nil
	case gomodel.TypeExternal:
		return fmt.Errorf("external type %q is not implemented yet", typ.AIDLName)
	default:
		return fmt.Errorf("unsupported type %q", typ.GoExpr)
	}
}

func (r *goRenderer) imports() []importSpec {
	set := map[string]importSpec{}
	add := func(path string, alias string) {
		key := alias + "\x00" + path
		set[key] = importSpec{Path: path, Alias: alias}
	}

	add(r.runtimeImportPath, "")
	if len(r.model.Interfaces) != 0 {
		add("context", "")
	}
	if r.needsFmtImport() {
		add("fmt", "")
	}
	if r.hasStableInterfaces() {
		add("errors", "")
		add("sync", "")
	}
	for _, p := range r.model.Parcelables {
		if p.Custom == nil {
			continue
		}
		add(p.Custom.GoPackage, p.Custom.ImportAlias)
	}
	for _, p := range r.model.Parcelables {
		for _, field := range p.Fields {
			r.addTypeImports(set, field.Type)
		}
	}
	for _, u := range r.model.Unions {
		for _, field := range u.Fields {
			r.addTypeImports(set, field.Type)
		}
	}
	for _, iface := range r.model.Interfaces {
		for _, method := range iface.Methods {
			if method.Return != nil {
				r.addTypeImports(set, method.Return.Type)
			}
			for _, arg := range method.Inputs {
				r.addTypeImports(set, arg.Type)
			}
			for _, arg := range method.Outputs {
				r.addTypeImports(set, arg.Type)
			}
		}
	}

	imports := make([]importSpec, 0, len(set))
	for _, imp := range set {
		imports = append(imports, imp)
	}
	sort.Slice(imports, func(i, j int) bool {
		if imports[i].Path == imports[j].Path {
			return imports[i].Alias < imports[j].Alias
		}
		return imports[i].Path < imports[j].Path
	})
	return imports
}

func (r *goRenderer) needsFmtImport() bool {
	if len(r.model.Interfaces) != 0 || len(r.model.Unions) != 0 {
		return true
	}
	for _, p := range r.model.Parcelables {
		if p.Custom == nil {
			return true
		}
	}
	return false
}

func (r *goRenderer) addTypeImports(set map[string]importSpec, typ *gomodel.Type) {
	if typ == nil {
		return
	}
	switch typ.Kind {
	case gomodel.TypeSlice, gomodel.TypeArray:
		r.addTypeImports(set, typ.Elem)
	case gomodel.TypeMap:
		r.addTypeImports(set, typ.Key)
		r.addTypeImports(set, typ.Value)
	case gomodel.TypeParcelable:
		if cfg, ok := r.customParcelables[typ.AIDLName]; ok && cfg.GoPackage != "" {
			alias := customImportAlias(cfg.GoPackage)
			set[alias+"\x00"+cfg.GoPackage] = importSpec{Path: cfg.GoPackage, Alias: alias}
			return
		}
		if path, alias, ok := r.generatedImportSpec(typ); ok {
			set[alias+"\x00"+path] = importSpec{Path: path, Alias: alias}
		}
	case gomodel.TypeEnum, gomodel.TypeUnion, gomodel.TypeInterface:
		if path, alias, ok := r.generatedImportSpec(typ); ok {
			set[alias+"\x00"+path] = importSpec{Path: path, Alias: alias}
		}
	case gomodel.TypeBinder, gomodel.TypeExternal:
		return
	}
}

func customImportAlias(goPackage string) string {
	goPackage = strings.TrimSuffix(goPackage, "/")
	parts := strings.Split(goPackage, "/")
	var base string
	switch len(parts) {
	case 0:
		base = "customparcelable"
	case 1:
		base = parts[0]
	default:
		base = parts[len(parts)-2] + "_" + parts[len(parts)-1]
	}

	var b strings.Builder
	for _, r := range strings.ToLower(base) {
		switch {
		case 'a' <= r && r <= 'z', '0' <= r && r <= '9':
			b.WriteRune(r)
		default:
			b.WriteByte('_')
		}
	}
	alias := b.String()
	if alias == "" {
		return "customparcelable"
	}
	if alias[0] >= '0' && alias[0] <= '9' {
		return "aidl_" + alias
	}
	return alias
}

func aidlImportAlias(aidlPackage string) string {
	alias := sanitizeImportAliasName(strings.ReplaceAll(aidlPackage, ".", "_"))
	if alias == "" {
		return "aidl_generated"
	}
	return "aidl_" + alias
}

func sanitizeImportAliasName(name string) string {
	name = strings.ToLower(name)
	name = sanitizeIdentifier(name)
	if name == "" {
		return "aidl"
	}
	if token.Lookup(name).IsKeyword() {
		name += "_aidl"
	}
	if name[0] >= '0' && name[0] <= '9' {
		name = "aidl_" + name
	}
	return name
}

func (r *goRenderer) generatedImportSpec(typ *gomodel.Type) (string, string, bool) {
	if typ == nil || typ.DeclPackage == "" || typ.DeclPackage == r.model.AIDLPackage || r.generatedImportRoot == "" {
		return "", "", false
	}
	path := strings.TrimSuffix(r.generatedImportRoot, "/") + "/" + filepath.ToSlash(generatedPackageDir(typ.DeclPackage))
	return path, aidlImportAlias(typ.DeclPackage), true
}

func (r *goRenderer) requiresGeneratedImports() bool {
	var check func(typ *gomodel.Type) bool
	check = func(typ *gomodel.Type) bool {
		if typ == nil {
			return false
		}
		switch typ.Kind {
		case gomodel.TypeSlice, gomodel.TypeArray:
			return check(typ.Elem)
		case gomodel.TypeMap:
			return check(typ.Key) || check(typ.Value)
		case gomodel.TypeParcelable, gomodel.TypeEnum, gomodel.TypeUnion, gomodel.TypeInterface:
			if _, _, ok := r.customParcelableConfig(typ); ok {
				return false
			}
			return typ.DeclPackage != "" && typ.DeclPackage != r.model.AIDLPackage
		default:
			return false
		}
	}
	for _, p := range r.model.Parcelables {
		for _, field := range p.Fields {
			if check(field.Type) {
				return true
			}
		}
	}
	for _, u := range r.model.Unions {
		for _, field := range u.Fields {
			if check(field.Type) {
				return true
			}
		}
	}
	for _, iface := range r.model.Interfaces {
		for _, c := range iface.Consts {
			if check(c.Type) {
				return true
			}
		}
		for _, method := range iface.Methods {
			if method.Return != nil && check(method.Return.Type) {
				return true
			}
			for _, arg := range method.Inputs {
				if check(arg.Type) {
					return true
				}
			}
			for _, arg := range method.Outputs {
				if check(arg.Type) {
					return true
				}
			}
		}
	}
	return false
}

func (r *goRenderer) hasStableInterfaces() bool {
	for _, iface := range r.model.Interfaces {
		if iface.Stable {
			return true
		}
	}
	return false
}

func outputPath(model *gomodel.File) string {
	dir := "."
	if model.AIDLPackage != "" {
		dir = generatedPackageDir(model.AIDLPackage)
	} else if model.SourcePath != "" {
		parent := filepath.Base(filepath.Dir(model.SourcePath))
		if parent != "" && parent != "." && parent != string(filepath.Separator) {
			dir = model.GoPackage
		}
	}
	base := "aidl_generated"
	if model.SourcePath != "" {
		base = strings.TrimSuffix(filepath.Base(model.SourcePath), filepath.Ext(model.SourcePath))
	}
	base = strings.ToLower(base) + "_aidl.go"
	return filepath.Join(dir, base)
}

func generatedPackageDir(aidlPackage string) string {
	if aidlPackage == "" {
		return ""
	}
	parts := strings.Split(aidlPackage, ".")
	for i, part := range parts {
		if part == "internal" {
			parts[i] = "internal_"
		}
	}
	return filepath.Join(parts...)
}

func (r *goRenderer) typeExpr(typ *gomodel.Type) string {
	if typ == nil {
		return ""
	}
	switch typ.Kind {
	case gomodel.TypeSlice:
		return "[]" + r.typeExpr(typ.Elem)
	case gomodel.TypeArray:
		return "[]" + r.typeExpr(typ.Elem)
	case gomodel.TypeMap:
		if typ.Key == nil || typ.Value == nil {
			return "map[any]any"
		}
		return fmt.Sprintf("map[%s]%s", r.typeExpr(typ.Key), r.typeExpr(typ.Value))
	case gomodel.TypeParcelable:
		if cfg, alias, ok := r.customParcelableConfig(typ); ok {
			name := alias + "." + cfg.GoType
			if typ.Nullable {
				return "*" + name
			}
			return name
		}
		return r.namedTypeExpr(typ)
	case gomodel.TypeEnum, gomodel.TypeUnion, gomodel.TypeInterface:
		return r.namedTypeExpr(typ)
	default:
		return typ.GoExpr
	}
}

func (r *goRenderer) namedTypeExpr(typ *gomodel.Type) string {
	if typ == nil {
		return ""
	}
	if typ.NamedGo == "" {
		return typ.GoExpr
	}
	name := typ.NamedGo
	if qualifier := r.generatedQualifier(typ); qualifier != "" {
		name = qualifier + name
	}
	if typ.Nullable && (typ.Kind == gomodel.TypeParcelable || typ.Kind == gomodel.TypeUnion) {
		return "*" + name
	}
	return name
}

func (r *goRenderer) generatedQualifier(typ *gomodel.Type) string {
	if _, alias, ok := r.generatedImportSpec(typ); ok {
		return alias + "."
	}
	return ""
}

func (r *goRenderer) zeroExpr(typ *gomodel.Type) string {
	if typ == nil {
		return "nil"
	}
	switch typ.Kind {
	case gomodel.TypeBool:
		return "false"
	case gomodel.TypeByte, gomodel.TypeChar, gomodel.TypeInt32, gomodel.TypeInt64, gomodel.TypeFloat32, gomodel.TypeFloat64, gomodel.TypeEnum:
		return fmt.Sprintf("%s(0)", r.typeExpr(typ))
	case gomodel.TypeString:
		if typ.Nullable {
			return "nil"
		}
		return `""`
	case gomodel.TypeFileDescriptor:
		return "binder.FileDescriptor{}"
	case gomodel.TypeParcelFileDescriptor:
		if typ.Nullable {
			return "nil"
		}
		return "binder.ParcelFileDescriptor{}"
	case gomodel.TypeParcelable, gomodel.TypeUnion:
		if typ.Nullable {
			return "nil"
		}
		return "*new(" + r.typeExpr(typ) + ")"
	case gomodel.TypeSlice, gomodel.TypeArray:
		return "nil"
	case gomodel.TypeMap:
		return "nil"
	case gomodel.TypeInterface, gomodel.TypeBinder:
		return "nil"
	default:
		if typ.Nullable {
			return "nil"
		}
		if r.typeExpr(typ) == "" {
			return "nil"
		}
		return r.typeExpr(typ) + "{}"
	}
}

func (r *goRenderer) isRawMapType(typ *gomodel.Type) bool {
	return typ != nil && typ.Kind == gomodel.TypeMap && typ.GoExpr == "map[any]any"
}

func (r *goRenderer) isValidMapKeyType(typ *gomodel.Type) bool {
	if typ == nil {
		return false
	}
	switch typ.Kind {
	case gomodel.TypeBool, gomodel.TypeByte, gomodel.TypeChar, gomodel.TypeInt32, gomodel.TypeInt64, gomodel.TypeFloat32, gomodel.TypeFloat64, gomodel.TypeString, gomodel.TypeEnum, gomodel.TypeBinder, gomodel.TypeInterface:
		return true
	default:
		return false
	}
}

func (r *goRenderer) nonNullableType(typ *gomodel.Type) *gomodel.Type {
	if typ == nil {
		return nil
	}
	copy := *typ
	copy.Nullable = false
	return &copy
}

func (r *goRenderer) dynamicWriteExpr(parcel string, value string, typ *gomodel.Type, registrar string) string {
	if typ == nil {
		return `fmt.Errorf("%w: nil dynamic type", binder.ErrBadParcelable)`
	}
	switch typ.Kind {
	case gomodel.TypeBool:
		return r.dynamicTaggedWriteExpr(parcel, "binder.ValueBoolean", fmt.Sprintf("%s.WriteBool(%s)", parcel, value))
	case gomodel.TypeByte:
		return r.dynamicTaggedWriteExpr(parcel, "binder.ValueByte", fmt.Sprintf("%s.WriteInt32(int32(%s))", parcel, value))
	case gomodel.TypeChar:
		return r.dynamicTaggedWriteExpr(parcel, "binder.ValueChar", fmt.Sprintf("%s.WriteInt32(int32(%s))", parcel, value))
	case gomodel.TypeInt32:
		return r.dynamicTaggedWriteExpr(parcel, "binder.ValueInteger", fmt.Sprintf("%s.WriteInt32(%s)", parcel, value))
	case gomodel.TypeInt64:
		return r.dynamicTaggedWriteExpr(parcel, "binder.ValueLong", fmt.Sprintf("%s.WriteInt64(%s)", parcel, value))
	case gomodel.TypeFloat32:
		return r.dynamicTaggedWriteExpr(parcel, "binder.ValueFloat", fmt.Sprintf("%s.WriteFloat32(%s)", parcel, value))
	case gomodel.TypeFloat64:
		return r.dynamicTaggedWriteExpr(parcel, "binder.ValueDouble", fmt.Sprintf("%s.WriteFloat64(%s)", parcel, value))
	case gomodel.TypeString:
		if typ.Nullable {
			return fmt.Sprintf(`func() error {
	if %s == nil {
		return %s.WriteInt32(int32(binder.ValueNull))
	}
	if err := %s.WriteInt32(int32(binder.ValueString)); err != nil {
		return err
	}
	return %s.WriteString(*%s)
}()`, value, parcel, parcel, parcel, value)
		}
		return r.dynamicTaggedWriteExpr(parcel, "binder.ValueString", fmt.Sprintf("%s.WriteString(%s)", parcel, value))
	case gomodel.TypeEnum:
		return r.dynamicTaggedWriteExpr(parcel, "binder.ValueInteger", r.writeExpr(parcel, value, typ, registrar))
	case gomodel.TypeParcelable, gomodel.TypeUnion:
		base := r.nonNullableType(typ)
		payloadValue := value
		nullCheck := ""
		nullReturn := ""
		if typ.Nullable {
			payloadValue = "*" + value
			nullCheck = fmt.Sprintf("if %s == nil {\n\t\treturn %s.WriteInt32(int32(binder.ValueNull))\n\t}\n\t", value, parcel)
		}
		nullReturn = fmt.Sprintf(`func() error {
	%sif err := %s.WriteInt32(int32(binder.ValueParcelable)); err != nil {
		return err
	}
	if err := %s.WriteString(%q); err != nil {
		return err
	}
	return %s
}()`, nullCheck, parcel, parcel, typ.AIDLName, r.writeExpr(parcel, payloadValue, base, registrar))
		return nullReturn
	case gomodel.TypeBinder, gomodel.TypeInterface:
		return fmt.Sprintf(`func() error {
	if %s == nil {
		return %s.WriteInt32(int32(binder.ValueNull))
	}
	if err := %s.WriteInt32(int32(binder.ValueIBinder)); err != nil {
		return err
	}
	return %s
}()`, value, parcel, parcel, r.writeExpr(parcel, value, typ, registrar))
	case gomodel.TypeMap:
		return fmt.Sprintf(`func() error {
	if %s == nil {
		return %s.WriteInt32(int32(binder.ValueNull))
	}
	if err := %s.WriteInt32(int32(binder.ValueMap)); err != nil {
		return err
	}
	return %s
}()`, value, parcel, parcel, r.writeExpr(parcel, value, typ, registrar))
	case gomodel.TypeSlice:
		if !typ.IsList {
			return fmt.Sprintf(`fmt.Errorf("%%w: dynamic arrays are not implemented for %s", binder.ErrUnsupported)`, r.typeExpr(typ))
		}
		return fmt.Sprintf(`func() error {
	if %s == nil {
		return %s.WriteInt32(int32(binder.ValueNull))
	}
	if err := %s.WriteInt32(int32(binder.ValueList)); err != nil {
		return err
	}
	return binder.WriteSlice(%s, %s, func(p *binder.Parcel, item %s) error { return %s })
}()`, value, parcel, parcel, parcel, value, r.typeExpr(typ.Elem), r.dynamicWriteExpr("p", "item", typ.Elem, registrar))
	default:
		return fmt.Sprintf(`fmt.Errorf("%%w: unsupported dynamic type %s", binder.ErrUnsupported)`, r.typeExpr(typ))
	}
}

func (r *goRenderer) dynamicReadExpr(parcel string, typ *gomodel.Type) string {
	if typ == nil {
		return `func() (any, error) { return nil, binder.ErrBadParcelable }()`
	}
	switch typ.Kind {
	case gomodel.TypeBool:
		return r.dynamicReadTaggedExpr(parcel, typ, "binder.ValueBoolean", fmt.Sprintf("%s.ReadBool()", parcel), false, "")
	case gomodel.TypeByte:
		return r.dynamicReadTaggedExpr(parcel, typ, "binder.ValueByte", fmt.Sprintf(`func() (%s, error) {
	value, err := %s.ReadInt32()
	return %s(value), err
}()`, r.typeExpr(typ), parcel, r.typeExpr(typ)), false, "")
	case gomodel.TypeChar:
		return r.dynamicReadTaggedExpr(parcel, typ, "binder.ValueChar", fmt.Sprintf(`func() (%s, error) {
	value, err := %s.ReadInt32()
	return %s(value), err
}()`, r.typeExpr(typ), parcel, r.typeExpr(typ)), false, "")
	case gomodel.TypeInt32:
		return r.dynamicReadTaggedExpr(parcel, typ, "binder.ValueInteger", fmt.Sprintf("%s.ReadInt32()", parcel), false, "")
	case gomodel.TypeInt64:
		return r.dynamicReadTaggedExpr(parcel, typ, "binder.ValueLong", fmt.Sprintf("%s.ReadInt64()", parcel), false, "")
	case gomodel.TypeFloat32:
		return r.dynamicReadTaggedExpr(parcel, typ, "binder.ValueFloat", fmt.Sprintf("%s.ReadFloat32()", parcel), false, "")
	case gomodel.TypeFloat64:
		return r.dynamicReadTaggedExpr(parcel, typ, "binder.ValueDouble", fmt.Sprintf("%s.ReadFloat64()", parcel), false, "")
	case gomodel.TypeString:
		body := fmt.Sprintf("%s.ReadString()", parcel)
		if typ.Nullable {
			body = fmt.Sprintf(`func() (*string, error) {
	value, err := %s.ReadString()
	if err != nil {
		return nil, err
	}
	return &value, nil
}()`, parcel)
			return r.dynamicReadTaggedExpr(parcel, typ, "binder.ValueString", body, true, "nil")
		}
		return r.dynamicReadTaggedExpr(parcel, typ, "binder.ValueString", body, false, "")
	case gomodel.TypeEnum:
		return r.dynamicReadTaggedExpr(parcel, typ, "binder.ValueInteger", r.readExpr(parcel, typ), false, "")
	case gomodel.TypeParcelable, gomodel.TypeUnion:
		base := r.nonNullableType(typ)
		body := r.readExpr(parcel, base)
		if typ.Nullable {
			body = fmt.Sprintf(`func() (*%s, error) {
	if _, err := %s.ReadString(); err != nil {
		return nil, err
	}
	value, err := %s
	if err != nil {
		return nil, err
	}
	return &value, nil
}()`, r.typeExpr(base), parcel, body)
			return r.dynamicReadTaggedExpr(parcel, typ, "binder.ValueParcelable", body, true, "nil")
		}
		body = fmt.Sprintf(`func() (%s, error) {
	if _, err := %s.ReadString(); err != nil {
		return %s, err
	}
	return %s
}()`, r.typeExpr(base), parcel, r.zeroExpr(base), body)
		return r.dynamicReadTaggedExpr(parcel, base, "binder.ValueParcelable", body, false, "")
	case gomodel.TypeBinder, gomodel.TypeInterface:
		return r.dynamicReadTaggedExpr(parcel, typ, "binder.ValueIBinder", r.readExpr(parcel, typ), true, "nil")
	case gomodel.TypeMap:
		return r.dynamicReadTaggedExpr(parcel, typ, "binder.ValueMap", r.readExpr(parcel, typ), true, "nil")
	case gomodel.TypeSlice:
		if !typ.IsList {
			return fmt.Sprintf(`func() (%s, error) { return %s, fmt.Errorf("%%w: dynamic arrays are not implemented for %s", binder.ErrUnsupported) }()`, r.typeExpr(typ), r.zeroExpr(typ), r.typeExpr(typ))
		}
		body := fmt.Sprintf("binder.ReadSlice(%s, func(p *binder.Parcel) (%s, error) { return %s })", parcel, r.typeExpr(typ.Elem), r.dynamicReadExpr("p", typ.Elem))
		return r.dynamicReadTaggedExpr(parcel, typ, "binder.ValueList", body, true, "nil")
	default:
		return fmt.Sprintf(`func() (%s, error) { return %s, fmt.Errorf("%%w: unsupported dynamic type %s", binder.ErrUnsupported) }()`, r.typeExpr(typ), r.zeroExpr(typ), r.typeExpr(typ))
	}
}

func (r *goRenderer) dynamicTaggedWriteExpr(parcel string, tag string, body string) string {
	return fmt.Sprintf(`func() error {
	if err := %s.WriteInt32(int32(%s)); err != nil {
		return err
	}
	return %s
}()`, parcel, tag, body)
}

func (r *goRenderer) dynamicReadTaggedExpr(parcel string, typ *gomodel.Type, tag string, body string, allowNull bool, nullValue string) string {
	if typ == nil {
		return `func() (any, error) { return nil, binder.ErrBadParcelable }()`
	}
	if nullValue == "" {
		nullValue = r.zeroExpr(typ)
	}
	nullCase := ""
	if allowNull {
		nullCase = fmt.Sprintf("case binder.ValueNull:\n\t\treturn %s, nil\n\t", nullValue)
	}
	return fmt.Sprintf(`func() (%s, error) {
	tagValue, err := %s.ReadInt32()
	if err != nil {
		return %s, err
	}
	switch binder.ValueTag(tagValue) {
	case %s:
		return %s
	%sdefault:
		return %s, fmt.Errorf("%%w: unexpected value tag %%d for %s", binder.ErrBadParcelable, tagValue)
	}
}()`, r.typeExpr(typ), parcel, r.zeroExpr(typ), tag, body, nullCase, r.zeroExpr(typ), r.typeExpr(typ))
}

func (r *goRenderer) renderEnum(enum *gomodel.Enum) {
	r.linef("type %s %s", enum.GoName, r.typeExpr(enum.Underlying))
	r.linef("")
	r.linef("const (")
	for _, member := range enum.Members {
		valueExpr := member.Value
		if valueExpr == "" {
			valueExpr = "0"
		}
		r.linef("\t%s %s = %s", member.GoName, enum.GoName, valueExpr)
	}
	r.linef(")")
	r.linef("")
	r.linef("func write%sToParcel(p *binder.Parcel, v %s) error {", enum.GoName, enum.GoName)
	r.linef("\treturn %s", r.writeExpr("p", enumCastValue(enum), enumTypeAsBacking(enum), ""))
	r.linef("}")
	r.linef("")
	r.linef("func read%sFromParcel(p *binder.Parcel) (%s, error) {", enum.GoName, enum.GoName)
	r.linef("\tv, err := %s", r.readExpr("p", enumTypeAsBacking(enum)))
	r.linef("\tif err != nil {")
	r.linef("\t\treturn 0, err")
	r.linef("\t}")
	r.linef("\treturn %s(v), nil", enum.GoName)
	r.linef("}")
	r.linef("")
	r.linef("func Write%sToParcel(p *binder.Parcel, v %s) error {", enum.GoName, enum.GoName)
	r.linef("\treturn write%sToParcel(p, v)", enum.GoName)
	r.linef("}")
	r.linef("")
	r.linef("func Read%sFromParcel(p *binder.Parcel) (%s, error) {", enum.GoName, enum.GoName)
	r.linef("\treturn read%sFromParcel(p)", enum.GoName)
	r.linef("}")
	r.linef("")
}

func enumTypeAsBacking(enum *gomodel.Enum) *gomodel.Type {
	return &gomodel.Type{
		Kind:      enum.Underlying.Kind,
		AIDLName:  enum.Underlying.AIDLName,
		GoExpr:    enum.Underlying.GoExpr,
		IsBuiltin: enum.Underlying.IsBuiltin,
	}
}

func enumCastValue(enum *gomodel.Enum) string {
	if enum == nil || enum.Underlying == nil {
		return "v"
	}
	return fmt.Sprintf("%s(v)", enum.Underlying.GoExpr)
}

