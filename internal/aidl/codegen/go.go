func (r *goRenderer) fileHelperPrefix() string {
	base := "aidlGenerated"
	if r.model != nil && r.model.SourcePath != "" {
		base = strings.TrimSuffix(filepath.Base(r.model.SourcePath), filepath.Ext(r.model.SourcePath))
	}
	prefix := lowerFirst(sanitizeIdentifier(base))
	if prefix == "" {
		return "aidlGenerated"
	}
	if prefix[0] >= '0' && prefix[0] <= '9' {
		return "aidl" + prefix
	}
	return prefix
}

func (r *goRenderer) customParcelableConfig(typ *gomodel.Type) (gomodel.CustomParcelableConfig, string, bool) {
	if typ == nil || typ.Kind != gomodel.TypeParcelable {
		return gomodel.CustomParcelableConfig{}, "", false
	}
	cfg, ok := r.customParcelables[typ.AIDLName]
	if ok && cfg.GoPackage != "" && cfg.GoType != "" && cfg.WriteFunc != "" && cfg.ReadFunc != "" {
		return cfg, customImportAlias(cfg.GoPackage), true
	}
	if typ.NamedGo != "" {
		if p := r.parcelablesByName[typ.NamedGo]; p != nil && p.Custom != nil {
			return gomodel.CustomParcelableConfig{
				AIDLName:  typ.AIDLName,
				GoPackage: p.Custom.GoPackage,
				GoType:    p.Custom.GoType,
				WriteFunc: p.Custom.WriteFunc,
				ReadFunc:  p.Custom.ReadFunc,
				Nullable:  p.Custom.Nullable,
			}, p.Custom.ImportAlias, true
		}
	}
	return gomodel.CustomParcelableConfig{}, "", false
}

func (r *goRenderer) customParcelableWriteExpr(parcel string, value string, typ *gomodel.Type, cfg gomodel.CustomParcelableConfig, alias string) string {
	writeCall := fmt.Sprintf("%s.%s(%s, %s)", alias, cfg.WriteFunc, parcel, value)
	if !typ.Nullable {
		return writeCall
	}
	return fmt.Sprintf(`func() error {
	if %s == nil {
		return %s.WriteInt32(0)
	}
	if err := %s.WriteInt32(1); err != nil {
		return err
	}
	return %s.%s(%s, *%s)
}()`, value, parcel, parcel, alias, cfg.WriteFunc, parcel, value)
}

func (r *goRenderer) customParcelableReadExpr(parcel string, typ *gomodel.Type, cfg gomodel.CustomParcelableConfig, alias string) string {
	readCall := fmt.Sprintf("%s.%s(%s)", alias, cfg.ReadFunc, parcel)
	if !typ.Nullable {
		return readCall
	}
	return fmt.Sprintf(`func() (%s, error) {
	present, err := %s.ReadInt32()
	if err != nil {
		return nil, err
	}
	if present == 0 {
		return nil, nil
	}
	value, err := %s.%s(%s)
	if err != nil {
		return nil, err
	}
	return &value, nil
}()`, typ.GoExpr, parcel, alias, cfg.ReadFunc, parcel)
}

func registrarExpr(name string) string {
	if name == "" {
		return "nil"
	}
	return name
}

func (r *goRenderer) methodNeedsRegistrar(fields []gomodel.Field) bool {
	return r.fieldsNeedRegistrar(fields)
}

func (r *goRenderer) fieldsNeedRegistrar(fields []gomodel.Field) bool {
	for _, field := range fields {
		if r.typeNeedsRegistrar(field.Type) {
			return true
		}
	}
	return false
}

func (r *goRenderer) typeNeedsRegistrar(typ *gomodel.Type) bool {
	return r.typeNeedsRegistrarSeen(typ, map[string]struct{}{})
}

func (r *goRenderer) typeNeedsRegistrarSeen(typ *gomodel.Type, seen map[string]struct{}) bool {
	if typ == nil {
		return false
	}
	switch typ.Kind {
	case gomodel.TypeInterface:
		return true
	case gomodel.TypeSlice, gomodel.TypeArray:
		return r.typeNeedsRegistrarSeen(typ.Elem, seen)
	case gomodel.TypeMap:
		return r.typeNeedsRegistrarSeen(typ.Key, seen) || r.typeNeedsRegistrarSeen(typ.Value, seen)
	case gomodel.TypeParcelable:
		if _, _, ok := r.customParcelableConfig(typ); ok {
			return false
		}
		if typ.DeclPackage != "" && typ.DeclPackage != r.model.AIDLPackage {
			return true
		}
		if typ.NamedGo == "" {
			return false
		}
		key := "parcelable:" + typ.NamedGo
		if _, ok := seen[key]; ok {
			return false
		}
		seen[key] = struct{}{}
		defer delete(seen, key)
		p := r.parcelablesByName[typ.NamedGo]
		if p == nil {
			return false
		}
		return r.fieldsNeedRegistrarSeen(p.Fields, seen)
	case gomodel.TypeUnion:
		if typ.DeclPackage != "" && typ.DeclPackage != r.model.AIDLPackage {
			return true
		}
		if typ.NamedGo == "" {
			return false
		}
		key := "union:" + typ.NamedGo
		if _, ok := seen[key]; ok {
			return false
		}
		seen[key] = struct{}{}
		defer delete(seen, key)
		u := r.unionsByName[typ.NamedGo]
		if u == nil {
			return false
		}
		return r.fieldsNeedRegistrarSeen(u.Fields, seen)
	default:
		return false
	}
}

func (r *goRenderer) fieldsNeedRegistrarSeen(fields []gomodel.Field, seen map[string]struct{}) bool {
	for _, field := range fields {
		if r.typeNeedsRegistrarSeen(field.Type, seen) {
			return true
		}
	}
	return false
}

func (r *goRenderer) linef(format string, args ...any) {
	if len(args) == 0 {
		r.buf.WriteString(format)
		r.buf.WriteByte('\n')
		return
	}
	fmt.Fprintf(&r.buf, format+"\n", args...)
}
