func (r *goRenderer) renderUnion(u *gomodel.Union) {
	needsRegistrar := r.fieldsNeedRegistrar(u.Fields)
	tagType := u.GoName + "Tag"

	r.linef("type %s int32", tagType)
	r.linef("")
	r.linef("const (")
	for i, field := range u.Fields {
		r.linef("\t%sTag%s %s = %d", u.GoName, field.GoName, tagType, i)
	}
	r.linef(")")
	r.linef("")

	r.linef("type %s struct {", u.GoName)
	r.linef("\tTag %s", tagType)
	for _, field := range u.Fields {
		r.linef("\t%s %s", field.GoName, r.typeExpr(field.Type))
	}
	r.linef("}")
	r.linef("")

	if needsRegistrar {
		r.linef("func write%sToParcel(p *binder.Parcel, v %s) error {", u.GoName, u.GoName)
		r.linef("\treturn write%sToParcelWithRegistrar(p, nil, v)", u.GoName)
		r.linef("}")
		r.linef("")
		r.linef("func write%sToParcelWithRegistrar(p *binder.Parcel, registrar binder.LocalHandlerRegistrar, v %s) error {", u.GoName, u.GoName)
		r.linef("\tif err := p.WriteInt32(int32(v.Tag)); err != nil {")
		r.linef("\t\treturn err")
		r.linef("\t}")
		r.linef("\tswitch v.Tag {")
		for _, field := range u.Fields {
			r.linef("\tcase %sTag%s:", u.GoName, field.GoName)
			r.linef("\t\treturn %s", r.contextWriteExpr("p", "v."+field.GoName, field.Type, "registrar"))
		}
		r.linef("\tdefault:")
		r.linef("\t\treturn fmt.Errorf(\"%%w: invalid %s tag %%d\", binder.ErrBadParcelable, v.Tag)", u.GoName)
		r.linef("\t}")
		r.linef("}")
		r.linef("")
	} else {
		r.linef("func write%sToParcel(p *binder.Parcel, v %s) error {", u.GoName, u.GoName)
		r.linef("\tif err := p.WriteInt32(int32(v.Tag)); err != nil {")
		r.linef("\t\treturn err")
		r.linef("\t}")
		r.linef("\tswitch v.Tag {")
		for _, field := range u.Fields {
			r.linef("\tcase %sTag%s:", u.GoName, field.GoName)
			r.linef("\t\treturn %s", r.contextWriteExpr("p", "v."+field.GoName, field.Type, ""))
		}
		r.linef("\tdefault:")
		r.linef("\t\treturn fmt.Errorf(\"%%w: invalid %s tag %%d\", binder.ErrBadParcelable, v.Tag)", u.GoName)
		r.linef("\t}")
		r.linef("}")
		r.linef("")
	}

	r.linef("func read%sFromParcel(p *binder.Parcel) (%s, error) {", u.GoName, u.GoName)
	r.linef("\ttagValue, err := p.ReadInt32()")
	r.linef("\tif err != nil {")
	r.linef("\t\treturn %s{}, err", u.GoName)
	r.linef("\t}")
	r.linef("\tv := %s{Tag: %s(tagValue)}", u.GoName, tagType)
	r.linef("\tswitch v.Tag {")
	for _, field := range u.Fields {
		r.linef("\tcase %sTag%s:", u.GoName, field.GoName)
		r.linef("\t\tdecoded, err := %s", r.contextReadExpr("p", field.Type))
		r.linef("\t\tif err != nil {")
		r.linef("\t\t\treturn %s{}, err", u.GoName)
		r.linef("\t\t}")
		r.linef("\t\tv.%s = decoded", field.GoName)
		r.linef("\t\treturn v, nil")
	}
	r.linef("\tdefault:")
	r.linef("\t\treturn %s{}, fmt.Errorf(\"%%w: invalid %s tag %%d\", binder.ErrBadParcelable, v.Tag)", u.GoName, u.GoName)
	r.linef("\t}")
	r.linef("}")
	r.linef("")

	if needsRegistrar {
		r.linef("func writeNullable%sToParcel(p *binder.Parcel, v *%s) error {", u.GoName, u.GoName)
		r.linef("\treturn writeNullable%sToParcelWithRegistrar(p, nil, v)", u.GoName)
		r.linef("}")
		r.linef("")
		r.linef("func writeNullable%sToParcelWithRegistrar(p *binder.Parcel, registrar binder.LocalHandlerRegistrar, v *%s) error {", u.GoName, u.GoName)
		r.linef("\tif v == nil {")
		r.linef("\t\treturn p.WriteInt32(0)")
		r.linef("\t}")
		r.linef("\tif err := p.WriteInt32(1); err != nil {")
		r.linef("\t\treturn err")
		r.linef("\t}")
		r.linef("\treturn write%sToParcelWithRegistrar(p, registrar, *v)", u.GoName)
		r.linef("}")
		r.linef("")
	} else {
		r.linef("func writeNullable%sToParcel(p *binder.Parcel, v *%s) error {", u.GoName, u.GoName)
		r.linef("\tif v == nil {")
		r.linef("\t\treturn p.WriteInt32(0)")
		r.linef("\t}")
		r.linef("\tif err := p.WriteInt32(1); err != nil {")
		r.linef("\t\treturn err")
		r.linef("\t}")
		r.linef("\treturn write%sToParcel(p, *v)", u.GoName)
		r.linef("}")
		r.linef("")
	}

	r.linef("func readNullable%sFromParcel(p *binder.Parcel) (*%s, error) {", u.GoName, u.GoName)
	r.linef("\tpresent, err := p.ReadInt32()")
	r.linef("\tif err != nil {")
	r.linef("\t\treturn nil, err")
	r.linef("\t}")
	r.linef("\tif present == 0 {")
	r.linef("\t\treturn nil, nil")
	r.linef("\t}")
	r.linef("\tv, err := read%sFromParcel(p)", u.GoName)
	r.linef("\tif err != nil {")
	r.linef("\t\treturn nil, err")
	r.linef("\t}")
	r.linef("\treturn &v, nil")
	r.linef("}")
	r.linef("")
	r.renderUnionExports(u, needsRegistrar)
}

func (r *goRenderer) renderInterface(iface *gomodel.Interface) {
	r.linef("const %sDescriptor = %q", iface.GoName, iface.Descriptor)
	if iface.Stable {
		r.linef("const %sInterfaceVersion int32 = %d", iface.GoName, iface.Version)
		r.linef("const %sInterfaceHash = %q", iface.GoName, iface.Hash)
	}
	for _, c := range iface.Consts {
		r.linef("const %s %s = %s", c.GoName, r.typeExpr(c.Type), c.Value)
	}
	for _, method := range iface.Methods {
		r.linef("const %sTransaction%s uint32 = %d", iface.GoName, method.GoName, method.TransactionCode)
	}
	r.linef("")

	r.linef("type %s interface {", iface.GoName)
	for _, method := range iface.Methods {
		r.linef("\t%s(%s) (%s)", method.GoName, r.methodInputs(method), r.methodReturns(method))
	}
	r.linef("}")
	r.linef("")

	clientType := lowerFirst(iface.GoName) + "Client"
	r.linef("type %s struct {", clientType)
	r.linef("\ttarget binder.Binder")
	if iface.Stable {
		r.linef("\tversionMu sync.Mutex")
		r.linef("\tversionResolved bool")
		r.linef("\tcachedVersion int32")
		r.linef("\thashMu sync.Mutex")
		r.linef("\thashResolved bool")
		r.linef("\tcachedHash string")
	}
	r.linef("}")
	r.linef("")
	r.linef("func New%sClient(target binder.Binder) %s {", iface.GoName, iface.GoName)
	r.linef("\tif target == nil {")
	r.linef("\t\treturn nil")
	r.linef("\t}")
	if iface.Stable {
		r.linef("\treturn &%s{target: target, cachedVersion: -1, cachedHash: \"-1\"}", clientType)
	} else {
		r.linef("\treturn &%s{target: target}", clientType)
	}
	r.linef("}")
	r.linef("")
	r.linef("func (c *%s) AsBinder() binder.Binder {", clientType)
	r.linef("\tif c == nil {")
	r.linef("\t\treturn nil")
	r.linef("\t}")
	r.linef("\treturn c.target")
	r.linef("}")
	r.linef("")
	if iface.Stable {
		r.renderStableInterfaceHelpers(iface, clientType)
	}
	r.linef("func write%sToParcel(p *binder.Parcel, registrar binder.LocalHandlerRegistrar, v %s) error {", iface.GoName, iface.GoName)
	r.linef("\tif v == nil {")
	r.linef("\t\treturn p.WriteStrongBinder(nil)")
	r.linef("\t}")
	r.linef("\tif provider, ok := any(v).(binder.BinderProvider); ok {")
	r.linef("\t\treturn p.WriteStrongBinder(provider.AsBinder())")
	r.linef("\t}")
	r.linef("\tif registrar == nil {")
	r.linef("\t\treturn fmt.Errorf(\"%%w: no local handler registrar available for %s\", binder.ErrUnsupported)", iface.GoName)
	r.linef("\t}")
	r.linef("\tb, err := registrar.RegisterLocalHandler(New%sHandlerWithRegistrar(registrar, v))", iface.GoName)
	r.linef("\tif err != nil {")
	r.linef("\t\treturn err")
	r.linef("\t}")
	r.linef("\treturn p.WriteStrongBinder(b)")
	r.linef("}")
	r.linef("")
	r.linef("func read%sFromParcel(p *binder.Parcel) (%s, error) {", iface.GoName, iface.GoName)
	r.linef("\tb, err := p.ReadStrongBinder()")
	r.linef("\tif err != nil {")
	r.linef("\t\treturn nil, err")
	r.linef("\t}")
	r.linef("\tif b == nil {")
	r.linef("\t\treturn nil, nil")
	r.linef("\t}")
	r.linef("\treturn New%sClient(b), nil", iface.GoName)
	r.linef("}")
	r.linef("")
	r.linef("func Write%sToParcel(p *binder.Parcel, registrar binder.LocalHandlerRegistrar, v %s) error {", iface.GoName, iface.GoName)
	r.linef("\treturn write%sToParcel(p, registrar, v)", iface.GoName)
	r.linef("}")
	r.linef("")
	r.linef("func Read%sFromParcel(p *binder.Parcel) (%s, error) {", iface.GoName, iface.GoName)
	r.linef("\treturn read%sFromParcel(p)", iface.GoName)
	r.linef("}")
	r.linef("")

	for _, method := range iface.Methods {
		r.renderProxyMethod(iface, clientType, method)
	}

	handlerType := lowerFirst(iface.GoName) + "Handler"
	r.linef("type %s struct {", handlerType)
	r.linef("\timpl %s", iface.GoName)
	r.linef("\tregistrar binder.LocalHandlerRegistrar")
	r.linef("}")
	r.linef("")
	r.linef("func New%sHandler(impl %s) binder.Handler {", iface.GoName, iface.GoName)
	r.linef("\treturn New%sHandlerWithRegistrar(nil, impl)", iface.GoName)
	r.linef("}")
	r.linef("")
	r.linef("func New%sHandlerWithRegistrar(registrar binder.LocalHandlerRegistrar, impl %s) binder.Handler {", iface.GoName, iface.GoName)
	r.linef("\treturn &%s{impl: impl, registrar: registrar}", handlerType)
	r.linef("}")
	r.linef("")
	r.linef("func (h *%s) Descriptor() string {", handlerType)
	r.linef("\treturn %sDescriptor", iface.GoName)
	r.linef("}")
	r.linef("")
	if iface.Stable {
		r.linef("func (h *%s) InterfaceVersion() int32 {", handlerType)
		r.linef("\treturn %sInterfaceVersion", iface.GoName)
		r.linef("}")
		r.linef("")
		r.linef("func (h *%s) InterfaceHash() string {", handlerType)
		r.linef("\treturn %sInterfaceHash", iface.GoName)
		r.linef("}")
		r.linef("")
	}
	r.linef("func (h *%s) HandleTransact(ctx context.Context, code uint32, data *binder.Parcel) (*binder.Parcel, error) {", handlerType)
	r.linef("\tdescriptor, err := data.ReadInterfaceToken()")
	r.linef("\tif err != nil {")
	r.linef("\t\treturn nil, err")
	r.linef("\t}")
	r.linef("\tif descriptor != %sDescriptor {", iface.GoName)
	r.linef("\t\treturn nil, fmt.Errorf(\"%%w: descriptor %%q, want %%q\", binder.ErrBadParcelable, descriptor, %sDescriptor)", iface.GoName)
	r.linef("\t}")
	r.linef("\tswitch code {")
	for _, method := range iface.Methods {
		r.renderHandlerCase(iface, method)
	}
	r.linef("\tdefault:")
	r.linef("\t\treturn nil, fmt.Errorf(\"%%w: %s transaction code %%d\", binder.ErrUnknownTransaction, code)", iface.GoName)
	r.linef("\t}")
	r.linef("}")
	r.linef("")

	r.linef("func Check%sService(ctx context.Context, sm binder.ServiceManager, name string) (%s, error) {", iface.GoName, iface.GoName)
	r.linef("\tservice, err := sm.CheckService(ctx, name)")
	r.linef("\tif err != nil {")
	r.linef("\t\treturn nil, err")
	r.linef("\t}")
	r.linef("\treturn New%sClient(service), nil", iface.GoName)
	r.linef("}")
	r.linef("")
	r.linef("func Wait%sService(ctx context.Context, sm binder.ServiceManager, name string) (%s, error) {", iface.GoName, iface.GoName)
	r.linef("\tservice, err := sm.WaitService(ctx, name)")
	r.linef("\tif err != nil {")
	r.linef("\t\treturn nil, err")
	r.linef("\t}")
	r.linef("\treturn New%sClient(service), nil", iface.GoName)
	r.linef("}")
	r.linef("")
	r.linef("func Add%sService(ctx context.Context, sm binder.ServiceManager, name string, impl %s, opts ...binder.AddServiceOption) error {", iface.GoName, iface.GoName)
	r.linef("\tif registrar, ok := sm.(binder.LocalHandlerRegistrar); ok {")
	r.linef("\t\treturn sm.AddService(ctx, name, New%sHandlerWithRegistrar(registrar, impl), opts...)", iface.GoName)
	r.linef("\t}")
	r.linef("\treturn sm.AddService(ctx, name, New%sHandler(impl), opts...)", iface.GoName)
	r.linef("}")
	r.linef("")
}

func (r *goRenderer) renderStableInterfaceHelpers(iface *gomodel.Interface, clientType string) {
	r.linef("func query%sInterfaceVersion(ctx context.Context, target binder.Binder) (int32, error) {", iface.GoName)
	r.linef("\tversion, err := binder.GetInterfaceVersion(ctx, target)")
	r.linef("\tif err != nil {")
	r.linef("\t\tif errors.Is(err, binder.ErrUnknownTransaction) {")
	r.linef("\t\t\treturn -1, nil")
	r.linef("\t\t}")
	r.linef("\t\treturn -1, err")
	r.linef("\t}")
	r.linef("\treturn version, nil")
	r.linef("}")
	r.linef("")
	r.linef("func query%sInterfaceHash(ctx context.Context, target binder.Binder) (string, error) {", iface.GoName)
	r.linef("\thash, err := binder.GetInterfaceHash(ctx, target)")
	r.linef("\tif err != nil {")
	r.linef("\t\tif errors.Is(err, binder.ErrUnknownTransaction) {")
	r.linef("\t\t\treturn \"-1\", nil")
	r.linef("\t\t}")
	r.linef("\t\treturn \"\", err")
	r.linef("\t}")
	r.linef("\treturn hash, nil")
	r.linef("}")
	r.linef("")
	r.linef("func (c *%s) InterfaceVersion(ctx context.Context) (int32, error) {", clientType)
	r.linef("\tif c == nil || c.target == nil {")
	r.linef("\t\treturn -1, binder.ErrUnsupported")
	r.linef("\t}")
	r.linef("\tc.versionMu.Lock()")
	r.linef("\tdefer c.versionMu.Unlock()")
	r.linef("\tif c.versionResolved {")
	r.linef("\t\treturn c.cachedVersion, nil")
	r.linef("\t}")
	r.linef("\tversion, err := query%sInterfaceVersion(ctx, c.target)", iface.GoName)
	r.linef("\tif err != nil {")
	r.linef("\t\treturn -1, err")
	r.linef("\t}")
	r.linef("\tc.cachedVersion = version")
	r.linef("\tc.versionResolved = true")
	r.linef("\treturn version, nil")
	r.linef("}")
	r.linef("")
	r.linef("func (c *%s) InterfaceHash(ctx context.Context) (string, error) {", clientType)
	r.linef("\tif c == nil || c.target == nil {")
	r.linef("\t\treturn \"\", binder.ErrUnsupported")
	r.linef("\t}")
	r.linef("\tc.hashMu.Lock()")
	r.linef("\tdefer c.hashMu.Unlock()")
	r.linef("\tif c.hashResolved {")
	r.linef("\t\treturn c.cachedHash, nil")
	r.linef("\t}")
	r.linef("\thash, err := query%sInterfaceHash(ctx, c.target)", iface.GoName)
	r.linef("\tif err != nil {")
	r.linef("\t\treturn \"\", err")
	r.linef("\t}")
	r.linef("\tc.cachedHash = hash")
	r.linef("\tc.hashResolved = true")
	r.linef("\treturn hash, nil")
	r.linef("}")
	r.linef("")
	r.linef("func Get%sInterfaceVersion(ctx context.Context, v %s) (int32, error) {", iface.GoName, iface.GoName)
	r.linef("\tif v == nil {")
	r.linef("\t\treturn -1, binder.ErrUnsupported")
	r.linef("\t}")
	r.linef("\tif provider, ok := any(v).(interface {")
	r.linef("\t\tInterfaceVersion(context.Context) (int32, error)")
	r.linef("\t}); ok {")
	r.linef("\t\treturn provider.InterfaceVersion(ctx)")
	r.linef("\t}")
	r.linef("\tif provider, ok := any(v).(binder.InterfaceVersionProvider); ok {")
	r.linef("\t\treturn provider.InterfaceVersion(), nil")
	r.linef("\t}")
	r.linef("\tif provider, ok := any(v).(binder.BinderProvider); ok {")
	r.linef("\t\treturn query%sInterfaceVersion(ctx, provider.AsBinder())", iface.GoName)
	r.linef("\t}")
	r.linef("\treturn -1, binder.ErrUnsupported")
	r.linef("}")
	r.linef("")
	r.linef("func Get%sInterfaceHash(ctx context.Context, v %s) (string, error) {", iface.GoName, iface.GoName)
	r.linef("\tif v == nil {")
	r.linef("\t\treturn \"\", binder.ErrUnsupported")
	r.linef("\t}")
	r.linef("\tif provider, ok := any(v).(interface {")
	r.linef("\t\tInterfaceHash(context.Context) (string, error)")
	r.linef("\t}); ok {")
	r.linef("\t\treturn provider.InterfaceHash(ctx)")
	r.linef("\t}")
	r.linef("\tif provider, ok := any(v).(binder.InterfaceHashProvider); ok {")
	r.linef("\t\treturn provider.InterfaceHash(), nil")
	r.linef("\t}")
	r.linef("\tif provider, ok := any(v).(binder.BinderProvider); ok {")
	r.linef("\t\treturn query%sInterfaceHash(ctx, provider.AsBinder())", iface.GoName)
	r.linef("\t}")
	r.linef("\treturn \"\", binder.ErrUnsupported")
	r.linef("}")
	r.linef("")
}

func (r *goRenderer) renderProxyMethod(iface *gomodel.Interface, clientType string, method *gomodel.Method) {
	r.linef("func (c *%s) %s(%s) (%s) {", clientType, method.GoName, r.methodInputs(method), r.methodReturns(method))
	r.linef("\tif ctx == nil {")
	r.linef("\t\tctx = context.Background()")
	r.linef("\t}")
	r.linef("\treq := binder.NewParcel()")
	r.linef("\tif err := req.WriteInterfaceToken(%sDescriptor); err != nil {", iface.GoName)
	r.linef("\t\treturn %s", r.methodErrorReturn(method, "err"))
	r.linef("\t}")
	if r.methodNeedsRegistrar(method.Inputs) {
		r.linef("\tvar registrar binder.LocalHandlerRegistrar")
		r.linef("\tif resolved, ok := c.target.(binder.LocalHandlerRegistrar); ok {")
		r.linef("\t\tregistrar = resolved")
		r.linef("\t}")
	}
	for _, arg := range method.Inputs {
		r.linef("\tif err := %s; err != nil {", r.methodArgWriteExpr("req", arg.GoName, &arg, "registrar"))
		r.linef("\t\treturn %s", r.methodErrorReturn(method, "err"))
		r.linef("\t}")
	}
	flagExpr := "binder.FlagNone"
	if method.Oneway {
		flagExpr = "binder.FlagOneway"
	}
	if method.Oneway {
		r.linef("\t_, err := c.target.Transact(ctx, %sTransaction%s, req, %s)", iface.GoName, method.GoName, flagExpr)
		r.linef("\tif err != nil {")
		r.linef("\t\treturn %s", r.methodErrorReturn(method, "err"))
		r.linef("\t}")
		r.linef("\treturn nil")
		r.linef("}")
		r.linef("")
		return
	}
	r.linef("\tresp, err := c.target.Transact(ctx, %sTransaction%s, req, %s)", iface.GoName, method.GoName, flagExpr)
	r.linef("\tif err != nil {")
	r.linef("\t\treturn %s", r.methodErrorReturn(method, "err"))
	r.linef("\t}")
	r.linef("\tif resp == nil {")
	r.linef("\t\treturn %s", r.methodErrorReturn(method, "binder.ErrBadParcelable"))
	r.linef("\t}")
	r.linef("\tif err := binder.ReadException(resp); err != nil {")
	r.linef("\t\treturn %s", r.methodErrorReturn(method, "err"))
	r.linef("\t}")
	retVar := "ret"
	if method.Return != nil {
		r.linef("\t%s, err := %s", retVar, r.methodValueReadExpr("resp", method.Return.Type, r.methodUsesTypedObjectEncoding(method.Return.Type)))
		r.linef("\tif err != nil {")
		r.linef("\t\treturn %s", r.methodErrorReturn(method, "err"))
		r.linef("\t}")
	}
	outVars := make([]string, 0, len(method.Outputs))
	for _, out := range method.Outputs {
		outVar := resultLocalName(out.GoName)
		outVars = append(outVars, outVar)
		r.linef("\t%s, err := %s", outVar, r.methodValueReadExpr("resp", out.Type, true))
		r.linef("\tif err != nil {")
		r.linef("\t\treturn %s", r.methodErrorReturn(method, "err"))
		r.linef("\t}")
	}
	r.linef("\treturn %s", r.methodSuccessReturnVars(method, retVar, outVars))
	r.linef("}")
	r.linef("")
}

func (r *goRenderer) renderHandlerCase(iface *gomodel.Interface, method *gomodel.Method) {
	r.linef("\tcase %sTransaction%s:", iface.GoName, method.GoName)
	argVars := make([]string, 0, len(method.Inputs))
	for _, arg := range method.Inputs {
		argVar := argLocalName(arg.GoName)
		argVars = append(argVars, argVar)
		r.linef("\t\t%s, err := %s", argVar, r.methodArgReadExpr("data", &arg))
		r.linef("\t\tif err != nil {")
		r.linef("\t\t\treturn nil, err")
		r.linef("\t\t}")
	}
	callArgs := make([]string, 0, 1+len(method.Inputs))
	callArgs = append(callArgs, "ctx")
	callArgs = append(callArgs, argVars...)
	if method.Oneway {
		r.linef("\t\treturn nil, h.impl.%s(%s)", method.GoName, strings.Join(callArgs, ", "))
		return
	}

	resultVars := make([]string, 0, len(method.Outputs)+2)
	retVar := "ret"
	if method.Return != nil {
		resultVars = append(resultVars, retVar)
	}
	outVars := make([]string, 0, len(method.Outputs))
	for _, out := range method.Outputs {
		outVar := resultLocalName(out.GoName)
		outVars = append(outVars, outVar)
		resultVars = append(resultVars, outVar)
	}
	resultVars = append(resultVars, "err")
	assignOp := ":="
	if len(resultVars) == 1 && resultVars[0] == "err" {
		assignOp = "="
	}
	r.linef("\t\t%s %s h.impl.%s(%s)", strings.Join(resultVars, ", "), assignOp, method.GoName, strings.Join(callArgs, ", "))
	r.linef("\t\tif err != nil {")
	r.linef("\t\t\treply := binder.NewParcel()")
	r.linef("\t\t\thandled, writeErr := binder.TryWriteException(reply, err)")
	r.linef("\t\t\tif writeErr != nil {")
	r.linef("\t\t\t\treturn nil, writeErr")
	r.linef("\t\t\t}")
	r.linef("\t\t\tif handled {")
	r.linef("\t\t\t\treturn reply, nil")
	r.linef("\t\t\t}")
	r.linef("\t\t\treturn nil, err")
	r.linef("\t\t}")
	r.linef("\t\treply := binder.NewParcel()")
	r.linef("\t\tif err := binder.WriteNoException(reply); err != nil {")
	r.linef("\t\t\treturn nil, err")
	r.linef("\t\t}")
	if method.Return != nil {
		r.linef("\t\tif err := %s; err != nil {", r.methodValueWriteExpr("reply", retVar, method.Return.Type, "h.registrar", r.methodUsesTypedObjectEncoding(method.Return.Type)))
		r.linef("\t\t\treturn nil, err")
		r.linef("\t\t}")
	}
	for i, out := range method.Outputs {
		r.linef("\t\tif err := %s; err != nil {", r.methodValueWriteExpr("reply", outVars[i], out.Type, "h.registrar", true))
		r.linef("\t\t\treturn nil, err")
		r.linef("\t\t}")
	}
	r.linef("\t\treturn reply, nil")
}

func (r *goRenderer) methodInputs(method *gomodel.Method) string {
	parts := []string{"ctx context.Context"}
	for _, arg := range method.Inputs {
		parts = append(parts, fmt.Sprintf("%s %s", arg.GoName, r.methodInputTypeExpr(&arg)))
	}
	return strings.Join(parts, ", ")
}

func (r *goRenderer) methodReturns(method *gomodel.Method) string {
	if method.Oneway {
		return "error"
	}
	var parts []string
	if method.Return != nil {
		parts = append(parts, r.methodValueTypeExpr(method.Return.Type))
	}
	for _, out := range method.Outputs {
		parts = append(parts, r.methodValueTypeExpr(out.Type))
	}
	parts = append(parts, "error")
	return strings.Join(parts, ", ")
}

func (r *goRenderer) methodErrorReturn(method *gomodel.Method, errExpr string) string {
	if method.Oneway {
		return errExpr
	}
	var parts []string
	if method.Return != nil {
		parts = append(parts, r.methodValueZeroExpr(method.Return.Type))
	}
	for _, out := range method.Outputs {
		parts = append(parts, r.methodValueZeroExpr(out.Type))
	}
	parts = append(parts, errExpr)
	return strings.Join(parts, ", ")
}

func (r *goRenderer) methodSuccessReturn(method *gomodel.Method) string {
	if method.Oneway {
		return "nil"
	}
	return r.methodSuccessReturnVars(method, "ret", nil)
}

func (r *goRenderer) methodSuccessReturnVars(method *gomodel.Method, retVar string, outVars []string) string {
	if method.Oneway {
		return "nil"
	}
	var parts []string
	if method.Return != nil {
		parts = append(parts, retVar)
	}
	for i, out := range method.Outputs {
		if i < len(outVars) && outVars[i] != "" {
			parts = append(parts, outVars[i])
			continue
		}
		parts = append(parts, resultLocalName(out.GoName))
	}
	parts = append(parts, "nil")
	return strings.Join(parts, ", ")
}

func (r *goRenderer) methodArgWriteExpr(parcel string, value string, arg *gomodel.Field, registrar string) string {
	if arg == nil {
		return fmt.Sprintf("fmt.Errorf(\"%%w: nil method arg\", binder.ErrUnsupported)")
	}
	if r.methodInputUsesPointer(arg.Type) && !arg.Type.Nullable {
		return fmt.Sprintf(`func() error {
	if %s == nil {
		return fmt.Errorf("%%w: nil non-nullable argument %s", binder.ErrBadParcelable)
	}
	return %s
}()`, value, arg.GoName, r.contextWriteExpr(parcel, "*"+value, arg.Type, registrar))
	}
	return r.contextWriteExpr(parcel, value, arg.Type, registrar)
}

func (r *goRenderer) methodArgReadExpr(parcel string, arg *gomodel.Field) string {
	if arg == nil {
		return fmt.Sprintf("func() (any, error) { return nil, fmt.Errorf(\"%%w: nil method arg\", binder.ErrUnsupported) }()")
	}
	if r.methodInputUsesPointer(arg.Type) && !arg.Type.Nullable {
		return fmt.Sprintf(`func() (%s, error) {
	present, err := %s.ReadInt32()
	if err != nil {
		return nil, err
	}
	if present == 0 {
		return nil, binder.ErrBadParcelable
	}
	v, err := %s
	if err != nil {
		return nil, err
	}
	return &v, nil
}()`, r.methodInputTypeExpr(arg), parcel, r.readExpr(parcel, arg.Type))
	}
	return r.contextReadExpr(parcel, arg.Type)
}

func (r *goRenderer) methodInputTypeExpr(arg *gomodel.Field) string {
	if arg == nil || arg.Type == nil {
		return ""
	}
	if r.methodInputUsesPointer(arg.Type) && !arg.Type.Nullable {
		return "*" + r.typeExpr(arg.Type)
	}
	return r.typeExpr(arg.Type)
}

func (r *goRenderer) methodInputUsesPointer(typ *gomodel.Type) bool {
	if typ == nil {
		return false
	}
	switch typ.Kind {
	case gomodel.TypeParcelable, gomodel.TypeUnion:
		return true
	default:
		return false
	}
}

func (r *goRenderer) methodValueTypeExpr(typ *gomodel.Type) string {
	if typ == nil {
		return ""
	}
	if r.methodValueUsesPointer(typ) {
		base := r.nonNullableType(typ)
		return "*" + r.typeExpr(base)
	}
	return r.typeExpr(typ)
}

func (r *goRenderer) methodValueZeroExpr(typ *gomodel.Type) string {
	if typ == nil {
		return "nil"
	}
	if r.methodValueUsesPointer(typ) {
		return "nil"
	}
	return r.zeroExpr(typ)
}

func (r *goRenderer) methodValueUsesPointer(typ *gomodel.Type) bool {
	if typ == nil {
		return false
	}
	switch typ.Kind {
	case gomodel.TypeParcelable, gomodel.TypeUnion:
		return true
	default:
		return false
	}
}

func (r *goRenderer) methodValueWriteExpr(parcel string, value string, typ *gomodel.Type, registrar string, typedObject bool) string {
	if typ == nil {
		return fmt.Sprintf("fmt.Errorf(\"%%w: nil method value type\", binder.ErrUnsupported)")
	}
	if r.methodValueUsesPointer(typ) && !typ.Nullable {
		return fmt.Sprintf(`func() error {
	if %s == nil {
		return fmt.Errorf("%%w: nil non-nullable return value", binder.ErrBadParcelable)
	}
	return %s
}()`, value, r.contextWriteExpr(parcel, "*"+value, typ, registrar))
	}
	return r.contextWriteExpr(parcel, value, typ, registrar)
}

func (r *goRenderer) methodValueReadExpr(parcel string, typ *gomodel.Type, typedObject bool) string {
	if typ == nil {
		return fmt.Sprintf("func() (any, error) { return nil, fmt.Errorf(\"%%w: nil method value type\", binder.ErrUnsupported) }()")
	}
	if r.methodValueUsesPointer(typ) {
		base := r.nonNullableType(typ)
		if typ.Nullable {
			return r.contextReadExpr(parcel, typ)
		}
		return fmt.Sprintf(`func() (%s, error) {
	present, err := %s.ReadInt32()
	if err != nil {
		return nil, err
	}
	if present == 0 {
		return nil, binder.ErrBadParcelable
	}
	v, err := %s
	if err != nil {
		return nil, err
	}
	return &v, nil
}()`, r.methodValueTypeExpr(typ), parcel, r.readExpr(parcel, base))
	}
	return r.contextReadExpr(parcel, typ)
}

func (r *goRenderer) methodUsesTypedObjectEncoding(typ *gomodel.Type) bool {
	if typ == nil {
		return false
	}
	switch typ.Kind {
	case gomodel.TypeParcelable, gomodel.TypeUnion, gomodel.TypeParcelFileDescriptor:
		return true
	default:
		return false
	}
}

func (r *goRenderer) contextWriteExpr(parcel string, value string, typ *gomodel.Type, registrar string) string {
	if !r.methodUsesTypedObjectEncoding(typ) {
		return r.writeExpr(parcel, value, typ, registrar)
	}
	if typ.Nullable {
		return r.writeExpr(parcel, value, typ, registrar)
	}
	nonNullable := *typ
	nonNullable.Nullable = false
	return fmt.Sprintf(`func() error {
	if err := %s.WriteInt32(1); err != nil {
		return err
	}
	return %s
}()`, parcel, r.writeExpr(parcel, value, &nonNullable, registrar))
}

func (r *goRenderer) contextReadExpr(parcel string, typ *gomodel.Type) string {
	if !r.methodUsesTypedObjectEncoding(typ) {
		return r.readExpr(parcel, typ)
	}
	if typ.Nullable {
		return r.readExpr(parcel, typ)
	}
	nonNullable := *typ
	nonNullable.Nullable = false
	return fmt.Sprintf(`func() (%s, error) {
	present, err := %s.ReadInt32()
	if err != nil {
		return %s, err
	}
	if present == 0 {
		return %s, nil
	}
	return %s
}()`, r.typeExpr(typ), parcel, r.zeroExpr(typ), r.zeroExpr(typ), r.readExpr(parcel, &nonNullable))
}

func (r *goRenderer) writeExpr(parcel string, value string, typ *gomodel.Type, registrar string) string {
	switch typ.Kind {
	case gomodel.TypeBool:
		return fmt.Sprintf("%s.WriteBool(%s)", parcel, value)
	case gomodel.TypeByte:
		return fmt.Sprintf("%s.WriteByte(%s)", parcel, value)
	case gomodel.TypeChar:
		return fmt.Sprintf("%s.WriteChar(%s)", parcel, value)
	case gomodel.TypeInt32:
		return fmt.Sprintf("%s.WriteInt32(%s)", parcel, value)
	case gomodel.TypeInt64:
		return fmt.Sprintf("%s.WriteInt64(%s)", parcel, value)
	case gomodel.TypeFloat32:
		return fmt.Sprintf("%s.WriteFloat32(%s)", parcel, value)
	case gomodel.TypeFloat64:
		return fmt.Sprintf("%s.WriteFloat64(%s)", parcel, value)
	case gomodel.TypeString:
		if typ.Nullable {
			return fmt.Sprintf("%s.WriteNullableString(%s)", parcel, value)
		}
		return fmt.Sprintf("%s.WriteString(%s)", parcel, value)
	case gomodel.TypeParcelable:
		if cfg, alias, ok := r.customParcelableConfig(typ); ok {
			return r.customParcelableWriteExpr(parcel, value, typ, cfg, alias)
		}
		if qualifier := r.generatedQualifier(typ); qualifier != "" {
			if registrar != "" {
				if typ.Nullable {
					return fmt.Sprintf("%sWriteNullable%sToParcelWithRegistrar(%s, %s, %s)", qualifier, typ.NamedGo, parcel, registrarExpr(registrar), value)
				}
				return fmt.Sprintf("%sWrite%sToParcelWithRegistrar(%s, %s, %s)", qualifier, typ.NamedGo, parcel, registrarExpr(registrar), value)
			}
			if typ.Nullable {
				return fmt.Sprintf("%sWriteNullable%sToParcel(%s, %s)", qualifier, typ.NamedGo, parcel, value)
			}
			return fmt.Sprintf("%sWrite%sToParcel(%s, %s)", qualifier, typ.NamedGo, parcel, value)
		}
		if r.typeNeedsRegistrar(typ) {
			if typ.Nullable {
				return fmt.Sprintf("writeNullable%sToParcelWithRegistrar(%s, %s, %s)", typ.NamedGo, parcel, registrarExpr(registrar), value)
			}
			return fmt.Sprintf("write%sToParcelWithRegistrar(%s, %s, %s)", typ.NamedGo, parcel, registrarExpr(registrar), value)
		}
		if typ.Nullable {
			return fmt.Sprintf("writeNullable%sToParcel(%s, %s)", typ.NamedGo, parcel, value)
		}
		return fmt.Sprintf("write%sToParcel(%s, %s)", typ.NamedGo, parcel, value)
	case gomodel.TypeEnum:
		if qualifier := r.generatedQualifier(typ); qualifier != "" {
			return fmt.Sprintf("%sWrite%sToParcel(%s, %s)", qualifier, typ.NamedGo, parcel, value)
		}
		return fmt.Sprintf("write%sToParcel(%s, %s)", typ.NamedGo, parcel, value)
	case gomodel.TypeUnion:
		if qualifier := r.generatedQualifier(typ); qualifier != "" {
			if registrar != "" {
				if typ.Nullable {
					return fmt.Sprintf("%sWriteNullable%sToParcelWithRegistrar(%s, %s, %s)", qualifier, typ.NamedGo, parcel, registrarExpr(registrar), value)
				}
				return fmt.Sprintf("%sWrite%sToParcelWithRegistrar(%s, %s, %s)", qualifier, typ.NamedGo, parcel, registrarExpr(registrar), value)
			}
			if typ.Nullable {
				return fmt.Sprintf("%sWriteNullable%sToParcel(%s, %s)", qualifier, typ.NamedGo, parcel, value)
			}
			return fmt.Sprintf("%sWrite%sToParcel(%s, %s)", qualifier, typ.NamedGo, parcel, value)
		}
		if r.typeNeedsRegistrar(typ) {
			if typ.Nullable {
				return fmt.Sprintf("writeNullable%sToParcelWithRegistrar(%s, %s, %s)", typ.NamedGo, parcel, registrarExpr(registrar), value)
			}
			return fmt.Sprintf("write%sToParcelWithRegistrar(%s, %s, %s)", typ.NamedGo, parcel, registrarExpr(registrar), value)
		}
		if typ.Nullable {
			return fmt.Sprintf("writeNullable%sToParcel(%s, %s)", typ.NamedGo, parcel, value)
		}
		return fmt.Sprintf("write%sToParcel(%s, %s)", typ.NamedGo, parcel, value)
	case gomodel.TypeBinder:
		return fmt.Sprintf("%s.WriteStrongBinder(%s)", parcel, value)
	case gomodel.TypeFileDescriptor:
		return fmt.Sprintf("%s.WriteFileDescriptor(%s)", parcel, value)
	case gomodel.TypeParcelFileDescriptor:
		if typ.Nullable {
			return fmt.Sprintf("%s(%s, %s)", r.nullableParcelFileDescriptorWriterName(), parcel, value)
		}
		return fmt.Sprintf("%s.WriteParcelFileDescriptor(%s)", parcel, value)
	case gomodel.TypeInterface:
		if qualifier := r.generatedQualifier(typ); qualifier != "" {
			return fmt.Sprintf("%sWrite%sToParcel(%s, %s, %s)", qualifier, typ.NamedGo, parcel, registrarExpr(registrar), value)
		}
		return fmt.Sprintf("write%sToParcel(%s, %s, %s)", typ.NamedGo, parcel, registrarExpr(registrar), value)
	case gomodel.TypeMap:
		if r.isRawMapType(typ) {
			return fmt.Sprintf("binder.WriteMap(%s, %s, func(p *binder.Parcel, item any) error { return binder.WriteDynamicValue(p, item) }, func(p *binder.Parcel, item any) error { return binder.WriteDynamicValue(p, item) })", parcel, value)
		}
		return fmt.Sprintf("binder.WriteMap(%s, %s, func(p *binder.Parcel, item %s) error { return %s }, func(p *binder.Parcel, item %s) error { return %s })", parcel, value, r.typeExpr(typ.Key), r.contextWriteExpr("p", "item", typ.Key, registrar), r.typeExpr(typ.Value), r.contextWriteExpr("p", "item", typ.Value, registrar))
	case gomodel.TypeSlice:
		return fmt.Sprintf("binder.WriteSlice(%s, %s, func(p *binder.Parcel, item %s) error { return %s })", parcel, value, r.typeExpr(typ.Elem), r.contextWriteExpr("p", "item", typ.Elem, registrar))
	case gomodel.TypeArray:
		return fmt.Sprintf("binder.WriteFixedSlice(%s, %s, %d, func(p *binder.Parcel, item %s) error { return %s })", parcel, value, typ.FixedLen, r.typeExpr(typ.Elem), r.contextWriteExpr("p", "item", typ.Elem, registrar))
	default:
		return fmt.Sprintf("fmt.Errorf(\"%%w: unsupported type %s\", binder.ErrUnsupported)", r.typeExpr(typ))
	}
}

func (r *goRenderer) readExpr(parcel string, typ *gomodel.Type) string {
	switch typ.Kind {
	case gomodel.TypeBool:
		return fmt.Sprintf("%s.ReadBool()", parcel)
	case gomodel.TypeByte:
		return fmt.Sprintf("%s.ReadByte()", parcel)
	case gomodel.TypeChar:
		return fmt.Sprintf("%s.ReadChar()", parcel)
	case gomodel.TypeInt32:
		return fmt.Sprintf("%s.ReadInt32()", parcel)
	case gomodel.TypeInt64:
		return fmt.Sprintf("%s.ReadInt64()", parcel)
	case gomodel.TypeFloat32:
		return fmt.Sprintf("%s.ReadFloat32()", parcel)
	case gomodel.TypeFloat64:
		return fmt.Sprintf("%s.ReadFloat64()", parcel)
	case gomodel.TypeString:
		if typ.Nullable {
			return fmt.Sprintf("%s.ReadNullableString()", parcel)
		}
		return fmt.Sprintf("%s.ReadString()", parcel)
	case gomodel.TypeParcelable:
		if cfg, alias, ok := r.customParcelableConfig(typ); ok {
			return r.customParcelableReadExpr(parcel, typ, cfg, alias)
		}
		if qualifier := r.generatedQualifier(typ); qualifier != "" {
			if typ.Nullable {
				return fmt.Sprintf("%sReadNullable%sFromParcel(%s)", qualifier, typ.NamedGo, parcel)
			}
			return fmt.Sprintf("%sRead%sFromParcel(%s)", qualifier, typ.NamedGo, parcel)
		}
		if typ.Nullable {
			return fmt.Sprintf("readNullable%sFromParcel(%s)", typ.NamedGo, parcel)
		}
		return fmt.Sprintf("read%sFromParcel(%s)", typ.NamedGo, parcel)
	case gomodel.TypeEnum:
		if qualifier := r.generatedQualifier(typ); qualifier != "" {
			return fmt.Sprintf("%sRead%sFromParcel(%s)", qualifier, typ.NamedGo, parcel)
		}
		return fmt.Sprintf("read%sFromParcel(%s)", typ.NamedGo, parcel)
	case gomodel.TypeUnion:
		if qualifier := r.generatedQualifier(typ); qualifier != "" {
			if typ.Nullable {
				return fmt.Sprintf("%sReadNullable%sFromParcel(%s)", qualifier, typ.NamedGo, parcel)
			}
			return fmt.Sprintf("%sRead%sFromParcel(%s)", qualifier, typ.NamedGo, parcel)
		}
		if typ.Nullable {
			return fmt.Sprintf("readNullable%sFromParcel(%s)", typ.NamedGo, parcel)
		}
		return fmt.Sprintf("read%sFromParcel(%s)", typ.NamedGo, parcel)
	case gomodel.TypeBinder:
		return fmt.Sprintf("%s.ReadStrongBinder()", parcel)
	case gomodel.TypeFileDescriptor:
		return fmt.Sprintf("%s.ReadFileDescriptor()", parcel)
	case gomodel.TypeParcelFileDescriptor:
		if typ.Nullable {
			return fmt.Sprintf("%s(%s)", r.nullableParcelFileDescriptorReaderName(), parcel)
		}
		return fmt.Sprintf("%s(%s)", r.requiredParcelFileDescriptorReaderName(), parcel)
	case gomodel.TypeInterface:
		if qualifier := r.generatedQualifier(typ); qualifier != "" {
			return fmt.Sprintf("%sRead%sFromParcel(%s)", qualifier, typ.NamedGo, parcel)
		}
		return fmt.Sprintf("read%sFromParcel(%s)", typ.NamedGo, parcel)
	case gomodel.TypeMap:
		if r.isRawMapType(typ) {
			return fmt.Sprintf("binder.ReadMap(%s, func(p *binder.Parcel) (any, error) { return binder.ReadDynamicValue(p) }, func(p *binder.Parcel) (any, error) { return binder.ReadDynamicValue(p) })", parcel)
		}
		return fmt.Sprintf("binder.ReadMap(%s, func(p *binder.Parcel) (%s, error) { return %s }, func(p *binder.Parcel) (%s, error) { return %s })", parcel, r.typeExpr(typ.Key), r.contextReadExpr("p", typ.Key), r.typeExpr(typ.Value), r.contextReadExpr("p", typ.Value))
	case gomodel.TypeSlice:
		return fmt.Sprintf("binder.ReadSlice(%s, func(p *binder.Parcel) (%s, error) { return %s })", parcel, r.typeExpr(typ.Elem), r.contextReadExpr("p", typ.Elem))
	case gomodel.TypeArray:
		return fmt.Sprintf("binder.ReadFixedSlice(%s, %d, func(p *binder.Parcel) (%s, error) { return %s })", parcel, typ.FixedLen, r.typeExpr(typ.Elem), r.contextReadExpr("p", typ.Elem))
	default:
		return fmt.Sprintf("func() (%s, error) { return %s, fmt.Errorf(\"%%w: unsupported type %s\", binder.ErrUnsupported) }()", r.typeExpr(typ), r.zeroExpr(typ), r.typeExpr(typ))
	}
}

func parseInt(value string) (int64, error) {
	value = strings.TrimSpace(value)
	if value == "" {
		return 0, fmt.Errorf("empty")
	}
	var sign int64 = 1
	if strings.HasPrefix(value, "-") {
		sign = -1
		value = strings.TrimPrefix(value, "-")
	}
	var n int64
	for _, r := range value {
		if r < '0' || r > '9' {
			return 0, fmt.Errorf("not decimal")
		}
		n = n*10 + int64(r-'0')
	}
	return sign * n, nil
}

